public class UsFlagNested {
            public static void main(String[] args) {
                for (int i = 0; i < 13; i++) {
                    if (i < 6) {
                        
                        for (int j = 0; j < 6; j++) {
                            System.out.print("* ");
                        }
                        for (int j = 0; j < 34; j++) {
                            System.out.print("=");
                        }
                    } else {
                        
                        for (int j = 0; j < 46; j++) {
                            System.out.print("=");
                        }
                    }
                    System.out.println(); 
                }
            }
        }
        
    
